#ifndef HELPER_BASICTYPES_H__
#define HELPER_BASICTYPES_H__
#include <cstdint>

namespace ME {

/**
* One byte variable type
*/
typedef char byte;


/**
* int 8 variable type
*/
typedef std::int8_t int8;

/**
* unsigned int 8 variable type
*/
typedef std::uint8_t uint8;

/**
* int 16 variable type
*/
typedef std::int16_t int16;

/**
* unsigned int 16 variable type
*/
typedef std::uint16_t uint16;

/**
* int 32 variable type
*/
typedef std::int32_t int32;

/**
* unsigned int 32 variable type
*/
typedef std::uint32_t uint32;

/**
* int 64 variable type
*/
typedef std::int64_t int64;

/**
* unsigned int 64 variable type
*/
typedef std::uint64_t uint64;

}

#endif // HELPER_BASICTYPES_H__